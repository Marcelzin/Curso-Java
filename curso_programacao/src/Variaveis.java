
public class Variaveis {

	public static void main(String[] args) {
		
		// Definição Informal
		// Em programação, uma variável é uma porção de memória RAM utilizada para armazenar dados durante a execução dos programas.
		// Estrutura de definição de variável:
		// <tipo> <nome> = <valor inicial>;
		
		String nome = "Marcel";
		int idade = 19;
		double altura = 1.82;
		char sexo = 'M';
		
		System.out.println("Meu nome é " + nome + " tenho " + idade + " anos de idade e " + altura + "cm de altura e sou do sexo " + sexo);

		/* Nomes de variáveis
		• Não pode começar com dígito: use uma letra ou _
		• Não pode ter espaço em branco
		• Não usar acentos ou til
		• Sugestão: use o padrão "camel case"
		Errado:
		int 5minutos;
		int salário;
		int salário do funcionario;
		Correto:
		int _5minutos;
		int salario;
		int salarioDoFuncionario; */
		
	}

}
